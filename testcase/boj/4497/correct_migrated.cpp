#include <iostream>
using namespace std;
int main() {
    int n;
    string s;
    char U[3][3],D[3][3],F[3][3],B[3][3],R[3][3],L[3][3],X[3][3];
    cin >> n;
    for(int i=0;i<n;i++) {
        for(int j=0;j<3;j++) {
            cin>>L[0][2-j]>>L[1][2-j]>>L[2][2-j]>>D[j][0]>>D[j][1]>>D[j][2]>>B[2-j][2]>>B[2-j][1]>>B[2-j][0]>>U[j][0]>>U[j][1]>>U[j][2]>>F[j][0]>>F[j][1]>>F[j][2]>>R[j][0]>>R[j][1]>>R[j][2];
        }
        cin>>s;
        for(int j=0;j<s.length();j++) {
            if(s[j]=='B') {
                for(int k=0;k<3;k++) {
                    X[0][k]=F[0][k];
                    F[0][k]=R[0][k];
                    R[0][k]=B[0][k];
                    B[0][k]=L[0][k];
                    L[0][k]=X[0][k];
                } X[0][0]=U[0][0];
                U[0][0]=U[2][0];
                U[2][0]=U[2][2];
                U[2][2]=U[0][2];
                U[0][2]=X[0][0];
                X[0][0]=U[1][0];
                U[1][0]=U[2][1];
                U[2][1]=U[1][2];
                U[1][2]=U[0][1];
                U[0][1]=X[0][0];
            } else if(s[j]=='O') {
                for(int k=0;k<3;k++) {
                    X[2][k]=F[2][k];
                    F[2][k]=L[2][k];
                    L[2][k]=B[2][k];
                    B[2][k]=R[2][k];
                    R[2][k]=X[2][k];
                } X[0][0]=D[0][0];
                D[0][0]=D[2][0];
                D[2][0]=D[2][2];
                D[2][2]=D[0][2];
                D[0][2]=X[0][0];
                X[0][0]=D[1][0];
                D[1][0]=D[2][1];
                D[2][1]=D[1][2];
                D[1][2]=D[0][1];
                D[0][1]=X[0][0];
            } else if(s[j]=='Y') {
                for(int k=0;k<3;k++) {
                    X[k][2]=F[k][2];
                    F[k][2]=D[k][2];
                    D[k][2]=B[2-k][0];
                    B[2-k][0]=U[k][2];
                    U[k][2]=X[k][2];
                } X[0][0]=R[0][0];
                R[0][0]=R[2][0];
                R[2][0]=R[2][2];
                R[2][2]=R[0][2];
                R[0][2]=X[0][0];
                X[0][0]=R[1][0];
                R[1][0]=R[2][1];
                R[2][1]=R[1][2];
                R[1][2]=R[0][1];
                R[0][1]=X[0][0];
            } else if(s[j]=='W') {
                for(int k=0;k<3;k++) {
                    X[k][0]=F[k][0];
                    F[k][0]=U[k][0];
                    U[k][0]=B[2-k][2];
                    B[2-k][2]=D[k][0];
                    D[k][0]=X[k][0];
                } X[0][0]=L[0][0];
                L[0][0]=L[2][0];
                L[2][0]=L[2][2];
                L[2][2]=L[0][2];
                L[0][2]=X[0][0];
                X[0][0]=L[1][0];
                L[1][0]=L[2][1];
                L[2][1]=L[1][2];
                L[1][2]=L[0][1];
                L[0][1]=X[0][0];
            } else if(s[j]=='G') {
                for(int k=0;k<3;k++) {
                    X[0][0]=U[2][k];
                    U[2][k]=L[2-k][2];
                    L[2-k][2]=D[0][2-k];
                    D[0][2-k]=R[k][0];
                    R[k][0]=X[0][0];
                } X[0][0]=F[0][0];
                F[0][0]=F[2][0];
                F[2][0]=F[2][2];
                F[2][2]=F[0][2];
                F[0][2]=X[0][0];
                X[0][0]=F[1][0];
                F[1][0]=F[2][1];
                F[2][1]=F[1][2];
                F[1][2]=F[0][1];
                F[0][1]=X[0][0];
            } else if(s[j]=='R') {
                for(int k=0;k<3;k++) {
                    X[0][0]=U[0][k];
                    U[0][k]=R[k][2];
                    R[k][2]=D[2][2-k];
                    D[2][2-k]=L[2-k][0];
                    L[2-k][0]=X[0][0];
                } X[0][0]=B[0][0];
                B[0][0]=B[2][0];
                B[2][0]=B[2][2];
                B[2][2]=B[0][2];
                B[0][2]=X[0][0];
                X[0][0]=B[1][0];
                B[1][0]=B[2][1];
                B[2][1]=B[1][2];
                B[1][2]=B[0][1];
                B[0][1]=X[0][0];
            }
        } for(int j=0;j<3;j++) {
            cout<<L[0][2-j]<<' '<<L[1][2-j]<<' '<<L[2][2-j]<<' '<<D[j][0]<<' '<<D[j][1]<<' '<<D[j][2]<<' '<<B[2-j][2]<<' '<<B[2-j][1]<<' '<<B[2-j][0]<<' '<<U[j][0]<<' '<<U[j][1]<<' '<<U[j][2]<<' '<<F[j][0]<<' '<<F[j][1]<<' '<<F[j][2]<<' '<<R[j][0]<<' '<<R[j][1]<<' '<<R[j][2]<<endl;
        } cout<<"==================================="<<endl;
    }
}
