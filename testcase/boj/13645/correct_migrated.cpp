#include <bits/stdc++.h>
using namespace std;
vector<vector<vector<int>>> C={{{1,1,1},{1,1,1},{1,1,1}},{{6,6,6},{6,6,6},{6,6,6}},{{3,3,3},{3,3,3},{3,3,3}},{{5,5,5},{5,5,5},{5,5,5}},{{2,2,2},{2,2,2},{2,2,2}},{{4,4,4},{4,4,4},{4,4,4}}};
int t;
void R(char c) {
    int n;
    if(c=='U'||c=='u') n=0;
    else if(c=='D'||c=='d') n=1;
    else if(c=='R'||c=='r') n=2;
    else if(c=='L'||c=='l') n=3;
    else if(c=='F'||c=='f') n=4;
    else n=5;
    if(c<'Z') {
        t=C[n][0][0];C[n][0][0]=C[n][2][0];C[n][2][0]=C[n][2][2];
        C[n][2][2]=C[n][0][2];C[n][0][2]=t;t=C[n][1][0];
        C[n][1][0]=C[n][2][1];C[n][2][1]=C[n][1][2];
        C[n][1][2]=C[n][0][1];C[n][0][1]=t;
    } else {
        t=C[n][0][0];C[n][0][0]=C[n][0][2];C[n][0][2]=C[n][2][2];
        C[n][2][2]=C[n][2][0];C[n][2][0]=t;t=C[n][1][0];
        C[n][1][0]=C[n][0][1];C[n][0][1]=C[n][1][2];
        C[n][1][2]=C[n][2][1];C[n][2][1]=t;
    } if(c=='U') {
        for(int i=0;i<3;i++) {
            t=C[4][0][i];C[4][0][i]=C[2][0][i];
            C[2][0][i]=C[5][0][i];C[5][0][i]=C[3][0][i];
            C[3][0][i]=t;
        }
    } else if(c=='u') {
        for(int i=0;i<3;i++) {
            t=C[4][0][i];C[4][0][i]=C[3][0][i];
            C[3][0][i]=C[5][0][i];C[5][0][i]=C[2][0][i];
            C[2][0][i]=t;
        }
    } else if(c=='D') {
        for(int i=0;i<3;i++) {
            t=C[4][2][i];C[4][2][i]=C[3][2][i];
            C[3][2][i]=C[5][2][i];C[5][2][i]=C[2][2][i];
            C[2][2][i]=t;
        }
    } else if(c=='d') {
        for(int i=0;i<3;i++) {
            t=C[4][2][i];C[4][2][i]=C[2][2][i];
            C[2][2][i]=C[5][2][i];C[5][2][i]=C[3][2][i];
            C[3][2][i]=t;
        }
    } else if(c=='R') {
        for(int i=0;i<3;i++) {
            t=C[4][i][2];C[4][i][2]=C[1][i][2];
            C[1][i][2]=C[5][2-i][0];C[5][2-i][0]=C[0][i][2];
            C[0][i][2]=t;
        }
    } else if(c=='r') {
        for(int i=0;i<3;i++) {
            t=C[4][i][2];C[4][i][2]=C[0][i][2];
            C[0][i][2]=C[5][2-i][0];C[5][2-i][0]=C[1][i][2];
            C[1][i][2]=t;
        }
    } else if(c=='L') {
        for(int i=0;i<3;i++) {
            t=C[4][i][0];C[4][i][0]=C[0][i][0];
            C[0][i][0]=C[5][2-i][2];C[5][2-i][2]=C[1][i][0];
            C[1][i][0]=t;
        }
    } else if(c=='l') {
        for(int i=0;i<3;i++) {
            t=C[4][i][0];C[4][i][0]=C[1][i][0];
            C[1][i][0]=C[5][2-i][2];C[5][2-i][2]=C[0][i][0];
            C[0][i][0]=t;
        }
    } else if(c=='F') {
        for(int i=0;i<3;i++) {
            t=C[0][2][i];C[0][2][i]=C[3][2-i][2];
            C[3][2-i][2]=C[1][0][2-i];C[1][0][2-i]=C[2][i][0];
            C[2][i][0]=t;
        }
    } else if(c=='f') {
        for(int i=0;i<3;i++) {
            t=C[0][2][i];C[0][2][i]=C[2][i][0];
            C[2][i][0]=C[1][0][2-i];C[1][0][2-i]=C[3][2-i][2];
            C[3][2-i][2]=t;
        }
    } else if(c=='B') {
        for(int i=0;i<3;i++) {
            t=C[0][0][i];C[0][0][i]=C[2][i][2];
            C[2][i][2]=C[1][2][2-i];C[1][2][2-i]=C[3][2-i][0];
            C[3][2-i][0]=t;
        }
    } else {
        for(int i=0;i<3;i++) {
            t=C[0][0][i];C[0][0][i]=C[3][2-i][0];
            C[3][2-i][0]=C[1][2][2-i];C[1][2][2-i]=C[2][i][2];
            C[2][i][2]=t;
        }
    }
}
void rot(string s) {for(char c:s) R(c);}
int main() {
    string s;
    bool x;
    while(cin>>s) {
        int k=0;
        while(1) {
            k++;
            rot(s);
            x=1;
            for(int i=0;i<6;i++) {
                int r=C[i][0][0];
                for(int j=0;j<3;j++) {
                    for(int k=0;k<3;k++) {
                        if(C[i][j][k]!=r) x=0;
                    }
                }
            } if(x) {
                cout<<k<<'\n';
                break;
            }
        }
    }
}
